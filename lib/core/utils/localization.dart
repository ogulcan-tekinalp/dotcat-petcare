  // Localized age text
  static String getAgeText(int months) {
    if (months < 1) {
      return currentLanguage == AppLanguage.tr ? 'Yeni doğan' : 
             currentLanguage == AppLanguage.de ? 'Neugeboren' : 
             currentLanguage == AppLanguage.es ? 'Recién nacido' : 
             currentLanguage == AppLanguage.ar ? 'حديث الولادة' : 'Newborn';
    } else if (months < 12) {
      return currentLanguage == AppLanguage.tr ? '$months aylık' : 
             currentLanguage == AppLanguage.de ? '$months Monate alt' : 
             currentLanguage == AppLanguage.es ? '$months meses' : 
             currentLanguage == AppLanguage.ar ? '$months أشهر' : '$months months old';
    } else {
      final years = months ~/ 12;
      final remainingMonths = months % 12;
      if (remainingMonths == 0) {
        return currentLanguage == AppLanguage.tr ? '$years yaşında' : 
               currentLanguage == AppLanguage.de ? '$years Jahre alt' : 
               currentLanguage == AppLanguage.es ? '$years años' : 
               currentLanguage == AppLanguage.ar ? '$years سنة' : '$years years old';
      }
      return currentLanguage == AppLanguage.tr ? '$years yıl $remainingMonths ay' : 
             currentLanguage == AppLanguage.de ? '$years J. $remainingMonths M.' : 
             currentLanguage == AppLanguage.es ? '$years a. $remainingMonths m.' : 
             currentLanguage == AppLanguage.ar ? '$years سنة $remainingMonths شهر' : '$years yr $remainingMonths mo';
    }
  }

